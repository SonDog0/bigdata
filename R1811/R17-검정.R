# 가설검정

추론 통계학에서 중요시 여기는 3가지 부분
1. 모집단 대표 가능성
2. 확률 분포의 모양
3. 신뢰할만한 결과인가

세상에는 다양 한 분야에 걸처 이론과 관념이 존재
이론과 관념은 정답이아니라 '이럴것이다' 라는 하나의 가설
따라서 불완전성을 내포함

불완전함을 채우기 위해 새로운 가설이 등장함

그런데, 새로운 가설이 이전가설보다 늘 합리적이고 신뢰적일까?

가설검정을 위한 6가지 절차
귀무가설/대립가설 설정 -> 유의수준 선정
->  검정 통계량 선택 -> 결정규칙 수립
=> 표본추출, 결론 도출 -> 결과해설


제 1종오류 : 귀무가설이 옳지만 귀무가설을 기각할 확률
제 2종 오류 : 대립가설이 옳지만 귀무가설을 채택할 확률

따라서, 해석능력, 경험이 어느정도 동반되어야 함


일반적으로 Z,t 검정 사용 
분산, 3개이상 평균비교 F 검정 카이스퀘어 검정




단일표본 t 검정 - 흔하게 사용되는 분석은 아님
단일 모집단의 평균값과 검정값을 비교할 때 사용

ex) 초등6학년 키 평균 160 => 30명 평균키와 비교

전국 고등학교 수학평균 45/30 이다.
