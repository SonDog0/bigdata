# 스팸메일이 반짝할인일 확률 0.4

P(스팸 & 반짝)/P(반짝) = ?
P(스팸|반짝) 
P(스팸)=0.1
P(반짝)=0.05
P(스팸 & 반짝)/P(스팸) = 0.4
P(반짝|스팸) = 0.4
