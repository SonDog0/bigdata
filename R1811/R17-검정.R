# 단일표본 t 검정
단일 모집단의 평균값과 검정값을 비교할 때 사용
=> 표준편차는 알려져 있지 않음

표준편차를 모를 때
t = (평균 - mu) /(표준편차/sqrt(표본수))

ex) 어떤 보험회사의 클레임 평균 처리비용이 60이라 할 때
d <- c(45,49,62,40,43,61,48,53,67,63,78,64,76,45,54,51,56,63,69,58,51,58,59,56,57,38)

(mean(d))/(sd(d)/sqrt(26))

t.test(d,mu=60,alternative = c('less'))
t.test(d,mu=60,conf.level = 0.99)
t.test(d,mu=60,alternative = c('great'))


