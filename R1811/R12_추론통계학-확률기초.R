# 베이즈 정리
P(A & B) = P(A)P(B|A)
P(A & B) = P(B)P(A|B)

P(A|B) = P(A)(B|A)/P(B)
따라서, P(Ai|B) = P(Ai)P(B|Ai)/sum(P(Ai)P(B|Ai))

# ex) 반짝을 받았을때 스팸일 확률 
